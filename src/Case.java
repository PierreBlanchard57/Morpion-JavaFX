
public class Case {

	private int _chiffre;
	private int _proprietaire;
	
	
	/**
	 * @param _chiffre
	 * @param _proprietaire
	 */
	public Case(int _chiffre, int _proprietaire) {
		this._chiffre = _chiffre;
		this._proprietaire = _proprietaire;
	}


	/**
	 * @return the _chiffre
	 */
	public int get_chiffre() {
		return _chiffre;
	}


	/**
	 * @param _chiffre the _chiffre to set
	 */
	public void set_chiffre(int _chiffre) {
		this._chiffre = _chiffre;
	}


	/**
	 * @return the _proprietaire
	 */
	public int get_proprietaire() {
		return _proprietaire;
	}


	/**
	 * @param _proprietaire the _proprietaire to set
	 */
	public void set_proprietaire(int _proprietaire) {
		this._proprietaire = _proprietaire;
	}

	
	
}
